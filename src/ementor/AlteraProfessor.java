    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       ConexoesMySQL conec = new ConexoesMySQL();
       Professor ProfessorEncontrado = conec.buscaProfessor(TID.getText());
       
       if (ProfessorEncontrado!=null){
           TNome.setText(ProfessorEncontrado.Nome);
           TDataNascimento.setText(ProfessorEncontrado.DataNascimento);
           TTelefone.setText(ProfessorEncontrado.Telefone);
           TDataAdmissao.setText(ProfessorEncontrado.getDataAdmissao());
           TSalarioBruto.setText(Double.toString(ProfessorEncontrado.getSalarioBruto()));
       }
       else{
           JOptionPane.showMessageDialog(null, "ID n√£o encontrado", "Busca", JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

